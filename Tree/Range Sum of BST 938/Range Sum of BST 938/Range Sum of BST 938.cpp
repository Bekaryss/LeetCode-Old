#include <iostream>
#include <vector>

using namespace std;

struct TreeNode {
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

int rangeSumBST(TreeNode* root, int L, int R) {
    int res = 0;
    dfs(root, L, R, res);
    return res;
}

void dfs(TreeNode* root, int L, int R, int& ans) {
    if (root != NULL) {
        if (L <= root->val && root->val <= R) {
            ans += root->val;
        }
        if (L < root->val)
            dfs(root->left, L, R, ans);
        if (R > root->val)
            dfs(root->right, L, R, ans);
    }
}

