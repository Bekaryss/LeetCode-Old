vector<double> sampleStats(vector<int>& count) {
	vector<double> res(5, -1);
	int maxOccur = 0, preSum = 0;
	long sumValues = 0, sumOccur = 0;
	for (int i = 0; i < count.size(); i++)
	{
		if (count[i] != 0) {
			if (res[0] == -1)
				res[0] = i;
			if (maxOccur < count[i]) {
				maxOccur = count[i];
				res[4] = i;
			}
			sumValues += (count[i] * i);
			sumOccur += count[i];
			res[1] = i;
		}
	}
	res[2] = (double)sumValues / sumOccur;
	for (int i = res[0]; i <= res[1]+1; i++) {
		preSum += count[i];
		if (sumOccur % 2 != 0 && preSum > sumOccur / 2) {
			res[3] = i;
			break;
		}
		else if (preSum >= sumOccur / 2) {
			if (preSum == sumOccur / 2)
				res[3] = (double)(2 * i + 1) / 2;
			else {
				res[3] == i;
			}
			break;
		}
	}
	return res;
}

